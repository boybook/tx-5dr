const LEGACY_THEME_ID_MAP: Record<string, SpectrumThemeId> = {
  openwebrx: 'openwebrx-turbo',
  'sdrpp-rainbow': 'sdrpp-turbo',
};

export function isSpectrumThemeId(value: unknown): value is SpectrumThemeId {
  return typeof value === 'string' && SPECTRUM_THEME_IDS.includes(value as SpectrumThemeId);
}

export function normalizeSpectrumThemeId(value: unknown): SpectrumThemeId {
  if (isSpectrumThemeId(value)) {
    return value;
  }
  if (typeof value === 'string' && value in LEGACY_THEME_ID_MAP) {
    return LEGACY_THEME_ID_MAP[value];
  }
  return DEFAULT_SPECTRUM_THEME_ID;
}

export function getSpectrumTheme(id: unknown): SpectrumTheme {
  return SPECTRUM_THEMES[normalizeSpectrumThemeId(id)];
}

export function getSafeSpectrumThemeCurve(id: unknown): SpectrumThemeCurve {
  return safeCurve(getSpectrumTheme(id).curve);
}

export function buildSpectrumThemeColorLut(id: unknown): Uint8Array {
  const theme = getSpectrumTheme(id);
  const sortedStops = [...theme.stops].sort((left, right) => left.at - right.at);
  const lut = new Uint8Array(256 * 4);

  for (let index = 0; index < 256; index += 1) {
    const t = index / 255;
    const leftStop = findLeftStop(sortedStops, t);
    lut[index * 4] = leftStop.color[0];
    lut[index * 4 + 1] = leftStop.color[1];
    lut[index * 4 + 2] = leftStop.color[2];
    lut[index * 4 + 3] = 255;
  }

  return lut;
}

export function getSpectrumThemePreviewGradient(id: unknown, direction = '180deg'): string {
  const theme = getSpectrumTheme(id);
  const previewStops = sampleStopsForPreview(theme.stops, 12)
    .map(({ at, color }) => `rgb(${color[0]} ${color[1]} ${color[2]}) ${Math.round(at * 100)}%`)
    .join(', ');
  return `linear-gradient(${direction}, ${previewStops})`;
}

function colorsToStops(colors: readonly SpectrumColor[]): SpectrumThemeStop[] {
  const denominator = Math.max(colors.length - 1, 1);
  return colors.map((color, index) => ({
    at: index / denominator,
    color,
  }));
}

// WSJT-X WFPalette interpolates source .pal files into 256 display colors with interval=256/(n-1).
function interpolateWsjtxPalette(sourceColors: readonly SpectrumColor[]): SpectrumColor[] {
  const colors = sourceColors.length >= 2 ? sourceColors : [[0, 0, 0], [255, 255, 255]] as const;
  const interval = 256 / (colors.length - 1);
  const result: SpectrumColor[] = [];

  for (let index = 0; index < 256; index += 1) {
    let prior = Math.floor(index / interval);
    if (prior >= colors.length - 1) {
      prior -= 1;
    }
    if (index === 255) {
      result.push(colors[colors.length - 1]);
      continue;
    }
    const next = Math.min(colors.length - 1, prior + 1);
    const increment = index - interval * prior;
    result.push([
      interpolateChannel(colors[prior][0], colors[next][0], increment / interval),
      interpolateChannel(colors[prior][1], colors[next][1], increment / interval),
      interpolateChannel(colors[prior][2], colors[next][2], increment / interval),
    ]);
  }

  return result;
}

function sampleStopsForPreview(stops: readonly SpectrumThemeStop[], maxStops: number): SpectrumThemeStop[] {
  if (stops.length <= maxStops) {
    return [...stops];
  }
  const sampled: SpectrumThemeStop[] = [];
  const denominator = maxStops - 1;
  for (let index = 0; index < maxStops; index += 1) {
    const sourceIndex = Math.round((index / denominator) * (stops.length - 1));
    sampled.push(stops[sourceIndex]);
  }
  return sampled;
}

function findLeftStop(stops: SpectrumThemeStop[], value: number): SpectrumThemeStop {
  let current = stops[0];
  for (const stop of stops) {
    if (stop.at <= value) {
      current = stop;
    }
  }
  return current;
}

function interpolateChannel(left: number, right: number, factor: number): number {
  return Math.round(left + (right - left) * clamp(factor, 0, 1));
}

function clamp(value: number, min: number, max: number): number {
  return Math.min(max, Math.max(min, value));
}
