export type SpectrumColor = readonly [number, number, number];

export interface SpectrumThemeStop {
  at: number;
  color: SpectrumColor;
}

export interface SpectrumThemeCurve {
  gamma: number;
  contrast: number;
  bias: number;
}

export interface SpectrumTheme {
  id: SpectrumThemeId;
  labelKey: string;
  stops: readonly SpectrumThemeStop[];
  curve: SpectrumThemeCurve;
}

