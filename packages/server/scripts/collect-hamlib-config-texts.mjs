import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptPath = fileURLToPath(import.meta.url);
const scriptDir = path.dirname(scriptPath);
const outputPath = path.resolve(scriptDir, '../../web/src/utils/hamlibConfigTextCatalog.ts');

const { HamLib } = await import('hamlib');

const labels = new Set();
const tooltips = new Set();
const rigs = HamLib.getSupportedRigs();

for (const rigInfo of rigs) {
  const rig = new HamLib(rigInfo.rigModel);

  try {
    const schema = typeof rig.getConfigSchema === 'function' ? rig.getConfigSchema() : [];
    for (const field of schema) {
      if (typeof field.label === 'string' && field.label.trim()) {
        labels.add(field.label.trim());
      }
      if (typeof field.tooltip === 'string' && field.tooltip.trim()) {
        tooltips.add(field.tooltip.trim());
      }
    }
  } finally {
    await rig.destroy().catch(() => {});
  }
}

const labelList = Array.from(labels).sort((left, right) => left.localeCompare(right));
const tooltipList = Array.from(tooltips).sort((left, right) => left.localeCompare(right));

const output = `// Generated by packages/server/scripts/collect-hamlib-config-texts.mjs
// Do not edit this file manually. Update the source script and rerun it instead.

export const HAMLIB_CONFIG_LABEL_CATALOG = ${JSON.stringify(labelList, null, 2)} as const;

export const HAMLIB_CONFIG_TOOLTIP_CATALOG = ${JSON.stringify(tooltipList, null, 2)} as const;

export const HAMLIB_CONFIG_TEXT_CATALOG_METADATA = {
  rigCount: ${rigs.length},
  labelCount: ${labelList.length},
  tooltipCount: ${tooltipList.length},
} as const;
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, output, 'utf8');

console.log(`Collected Hamlib config texts from ${rigs.length} rigs.`);
console.log(`Labels: ${labelList.length}, tooltips: ${tooltipList.length}`);
console.log(`Wrote catalog to ${outputPath}`);
